//! Старые методы

//! Получить эл-т через id : getElementById
//? У document есть метод getElementById
//? Если мы используем getElementById - мы получаем эл только один, он должен быть один на странице, уникальный

const box = document.getElementById('box');
console.log(box);

//!  getElementById - один, getElements - много
//? Если мы говорим коду получи эл через определённый tag или class, возникает ситуация, напроимер h2 тегов (классов) на странице может быть несколько, коду нужна конкретика
//? если обратиться в консоль то получаем не отделный эл-т а коллекцию [массив данных]
//? HTMLCollection по другому называется псевдомассивом
//? Псевдомассивы - это тоже самое что и массивы но без всех методов
//? Чтобы получить кнопку которая нас интересует - необходимо указать индекс того эл который мы хотим использовать
//? Получаем только 2-ю кнопку

//! Получить эл-т через tag : getElementsByTagName

//? Сделать это можно двумя способами:

//? Подставить индекс в переменную
const btns = document.getElementsByTagName('button')[1];
console.log(btns); //? получаем 2-ю кнопку

//? Подставить индекс там где я хочу ипользовать кнопку
