//! Получить эл-т через class :  getElementsByClassName

const circles = document.getElementsByClassName('circle')[2];
console.log(circles); //? получаем 3-й круг


//! Новые методы

//? Более новые методы позволяют получать эл-ты при помощи селекторов, селектором может называться что угодно

//! Получить эл-т через селектор :  querySelector
//? querySelector - позвляет получать 1-й (единственный) эл-т со страницы по заданному селектору
//? Этот метод удобно использовать когда у нас есть эл-т который емеет уникальный css селектор

const oneHeart = document.querySelector('.heart');
console.log(oneHeart);

//! Получить эл-т через селектор :  querySelectorAll
//? querySelectorAll - позвляет получать  все эл-ты со страницы по заданному селектору, для того чтобы получить 1й элемент необходимо указать индекс [0]
//? Если не задать индекс мы получим HTMLCollection (псевдомассивом)
//? querySelectorAll - имеет метод 

// const hearts = document.querySelectorAll('.heart')[1];
// console.log(hearts); //? получаем 2-е сердце


const hearts = document.querySelectorAll('.heart');

//? Выведим по очередно все эл-ты которые нах в псевдомассиве hearts
//? item - это каждый эл-т который будет нах в этом псевдомассиве 

hearts.forEach(item => {
  console.log(item);
});